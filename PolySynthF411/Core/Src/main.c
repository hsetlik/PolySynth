//SPI DMA Callbacks and helpers ------------------

void HAL_SPI_TxHalfCpltCallback(SPI_HandleTypeDef *hspi) {
	// when we're half finished sending, request new data in nextVBuf
	nextVBufNeeded = 1;

}

void HAL_SPI_TxCpltCallback(SPI_HandleTypeDef *hspi) {
	if (hspi == &hspi1) { // voices are done
		// switch around the new and old buffers but hold a pointer to the one we just finished
		uint16_t *justFinished = currentVBuf;
		currentVBuf = nextVBuf;
		nextVBuf = justFinished;
		// start the next DMA transmission right away
		if (!HAL_SPI_Transmit_DMA(&hspi1, (uint8_t*) currentVBuf,
		VOICE_CLOCK_BUF_SIZE * 2)) {
			Error_Handler();
		}
	} else if (hspi == &hspi3){
		// pull CS high again
		ILI9341_Unselect();
		// alert the processor
		disp_dma_finished(graphicsProc);

	}
}

void HAL_I2C_TxCpltCallback(I2C_HandleTypeDef *i2c) {

	if (i2c == &hi2c3) { // our DAC is done updating to the current channel
		//1. set the mux to charge the capacitors appropriately
		finishVoiceUpdate_DMA(currentDacVoice);
		// 2. check if we have another voice to update
		uint8_t v = nextVoiceToUpdate();
		// 3. Update the voice if needed, set the flag to indicate we're finished otherwise
		if (v < 6) { // we have another update to do
			currentDacVoice = v;
			startVoiceUpdate_DMA(i2c, &voiceLevels[v].currentData[0],
					&voiceLevels[v].prevData[0]);

		} else { // we're finished
			currentDacVoice = 0;
			dacTransmissionFinished = 1;

		}
	}
}

// Timer Callbacks--------------------------------------------------

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *timer) {

	if (timer == &htim9) {
		TickTimer_tick();
	} else if (timer == &htim4) {
		// ask the main process for new CV data
		dacLevelsNeeded = 1;
		// if we aren't already mid-transmission, check if we need to transmit anything
		if (dacTransmissionFinished) {
			uint8_t v = nextVoiceToUpdate();
			if (v < 6) {
				dacTransmissionFinished = 0;
				currentDacVoice = v;
				startVoiceUpdate_DMA(&hi2c3, &voiceLevels[v].currentData[0],
						&voiceLevels[v].prevData[0]);
			}
		}
	}
}

