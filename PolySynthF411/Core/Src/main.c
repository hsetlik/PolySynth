/* USER CODE BEGIN 0 */

uint8_t nextVoiceToUpdate() {
	uint8_t voice = currentDacVoice;
	for (uint8_t i = 0; i < 6; i++) {
		voice = (voice + 1) % 6;
		for (uint8_t cv = 0; cv < 7; cv++) {
			if (voiceLevels[voice].currentData[cv]
					!= voiceLevels[voice].prevData[cv])
				return voice;
		}
	}
	return 6;
}

